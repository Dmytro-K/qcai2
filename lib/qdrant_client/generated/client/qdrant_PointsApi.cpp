void qdrant_PointsApi::countPoints(
    const QString &collection_name,
    const ::qcai2::qdrant_client::OptionalParam<qdrant_ReadConsistency> &consistency,
    const ::qcai2::qdrant_client::OptionalParam<qint32> &timeout,
    const ::qcai2::qdrant_client::OptionalParam<qdrant_CountRequest> &qdrant_count_request)
{
    QString fullPath =
        QString(_serverConfigs["countPoints"][_serverIndices.value("countPoints")].URL() +
                "/collections/{collection_name}/points/count");

    if (_apiKeys.contains("api-key"))
    {
        addHeaders("api-key", _apiKeys.find("api-key").value());
    }

    if (!_bearerToken.isEmpty())
        addHeaders("Authorization", "Bearer " + _bearerToken);

    {
        QString collection_namePathParam("{");
        collection_namePathParam.append("collection_name").append("}");
        QString pathPrefix, pathSuffix, pathDelimiter;
        QString pathStyle = "simple";
        if (pathStyle == "")
            pathStyle = "simple";
        pathPrefix = getParamStylePrefix(pathStyle);
        pathSuffix = getParamStyleSuffix(pathStyle);
        pathDelimiter = getParamStyleDelimiter(pathStyle, "collection_name", false);
        QString paramString =
            (pathStyle == "matrix") ? pathPrefix + "collection_name" + pathSuffix : pathPrefix;
        fullPath.replace(
            collection_namePathParam,
            paramString +
                QUrl::toPercentEncoding(::qcai2::qdrant_client::toStringValue(collection_name)));
    }
    QString queryPrefix, querySuffix, queryDelimiter, queryStyle;
    if (consistency.hasValue())
    {
        queryStyle = "form";
        if (queryStyle == "")
            queryStyle = "form";
        queryPrefix = getParamStylePrefix(queryStyle);
        querySuffix = getParamStyleSuffix(queryStyle);
        queryDelimiter = getParamStyleDelimiter(queryStyle, "consistency", true);
        if (fullPath.indexOf("?") > 0)
            fullPath.append(queryPrefix);
        else
            fullPath.append("?");

        QString paramString = (queryStyle == "form" && true)      ? ""
                              : (queryStyle == "form" && !(true)) ? "consistency" + querySuffix
                                                                  : "";
        QJsonObject parameter = consistency.value().asJsonObject();
        qint32 count = 0;
        for (const QString &key : parameter.keys())
        {
            if (count > 0)
            {
                queryDelimiter = ((queryStyle == "form" || queryStyle == "deepObject") && true)
                                     ? "&"
                                     : getParamStyleDelimiter(queryStyle, key, true);
                paramString.append(queryDelimiter);
            }
            QString assignOperator;
            if (queryStyle == "form")
                assignOperator = (true) ? "=" : ",";
            else if (queryStyle == "deepObject")
                assignOperator = (true) ? "=" : "none";
            switch (parameter.value(key).type())
            {
                case QJsonValue::String: {
                    paramString.append(
                        ((queryStyle == "form")
                             ? key
                             : QString("consistency").append("[").append(key).append("]")) +
                        assignOperator + parameter.value(key).toString());
                    break;
                }
                case QJsonValue::Double: {
                    paramString.append(
                        ((queryStyle == "form")
                             ? key
                             : QString("consistency").append("[").append(key).append("]")) +
                        assignOperator + QString::number(parameter.value(key).toDouble()));
                    break;
                }
                case QJsonValue::Bool: {
                    paramString.append(
                        ((queryStyle == "form")
                             ? key
                             : QString("consistency").append("[").append(key).append("]")) +
                        assignOperator + QVariant(parameter.value(key).toBool()).toString());
                    break;
                }
                case QJsonValue::Array: {
                    paramString.append(
                        ((queryStyle == "form")
                             ? key
                             : QString("consistency").append("[").append(key).append("]")) +
                        assignOperator + QVariant(parameter.value(key).toArray()).toString());
                    break;
                }
                case QJsonValue::Object: {
                    paramString.append(
                        ((queryStyle == "form")
                             ? key
                             : QString("consistency").append("[").append(key).append("]")) +
                        assignOperator + QVariant(parameter.value(key).toObject()).toString());
                    break;
                }
                case QJsonValue::Null:
                case QJsonValue::Undefined:
                    break;
            }
            count++;
        }
        fullPath.append(paramString);
    }
    if (timeout.hasValue())
    {
        queryStyle = "form";
        if (queryStyle == "")
            queryStyle = "form";
        queryPrefix = getParamStylePrefix(queryStyle);
        querySuffix = getParamStyleSuffix(queryStyle);
        queryDelimiter = getParamStyleDelimiter(queryStyle, "timeout", true);
        if (fullPath.indexOf("?") > 0)
            fullPath.append(queryPrefix);
        else
            fullPath.append("?");

        fullPath.append(QUrl::toPercentEncoding("timeout"))
            .append(querySuffix)
            .append(QUrl::toPercentEncoding(
                ::qcai2::qdrant_client::toStringValue(timeout.stringValue())));
    }
    qdrant_HttpRequestWorker *worker = new qdrant_HttpRequestWorker(this, _manager);
    worker->setTimeOut(_timeOut);
    worker->setWorkingDirectory(_workingDirectory);
    worker->setResponseCompressionEnabled(_isResponseCompressionEnabled);
    worker->setRequestCompressionEnabled(_isRequestCompressionEnabled);
    qdrant_HttpRequestInput input(fullPath, "POST");

    if (qdrant_count_request.hasValue())
    {

        QByteArray output = qdrant_count_request.value().asJson().toUtf8();
        input.request_body.append(output);
    }
    for (auto keyValueIt = _defaultHeaders.keyValueBegin();
         keyValueIt != _defaultHeaders.keyValueEnd(); keyValueIt++)
    {
        input.headers.insert(keyValueIt->first, keyValueIt->second);
    }

    connect(worker, &qdrant_HttpRequestWorker::downloadProgress, this,
            &qdrant_PointsApi::countPointsProgress);
    connect(worker, &qdrant_HttpRequestWorker::on_execution_finished, this,
            &qdrant_PointsApi::countPointsCallback);
    connect(this, &qdrant_PointsApi::abortRequestsSignal, worker, &QObject::deleteLater);
    connect(worker, &QObject::destroyed, this, [this] {
        if (findChildren<qdrant_HttpRequestWorker *>().count() == 0)
        {
            Q_EMIT allPendingRequestsCompleted();
        }
    });

    worker->execute(&input);
}

