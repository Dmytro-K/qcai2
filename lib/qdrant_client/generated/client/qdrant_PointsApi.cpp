void qdrant_PointsApi::upsertPoints(
    const QString &collection_name, const ::qcai2::qdrant_client::OptionalParam<bool> &wait,
    const ::qcai2::qdrant_client::OptionalParam<qdrant_WriteOrdering> &ordering,
    const ::qcai2::qdrant_client::OptionalParam<qint32> &timeout,
    const ::qcai2::qdrant_client::OptionalParam<qdrant_PointInsertOperations>
        &qdrant_point_insert_operations)
{
    QString fullPath =
        QString(_serverConfigs["upsertPoints"][_serverIndices.value("upsertPoints")].URL() +
                "/collections/{collection_name}/points");

    if (_apiKeys.contains("api-key"))
    {
        addHeaders("api-key", _apiKeys.find("api-key").value());
    }

    if (!_bearerToken.isEmpty())
        addHeaders("Authorization", "Bearer " + _bearerToken);

    {
        QString collection_namePathParam("{");
        collection_namePathParam.append("collection_name").append("}");
        QString pathPrefix, pathSuffix, pathDelimiter;
        QString pathStyle = "simple";
        if (pathStyle == "")
            pathStyle = "simple";
        pathPrefix = getParamStylePrefix(pathStyle);
        pathSuffix = getParamStyleSuffix(pathStyle);
        pathDelimiter = getParamStyleDelimiter(pathStyle, "collection_name", false);
        QString paramString =
            (pathStyle == "matrix") ? pathPrefix + "collection_name" + pathSuffix : pathPrefix;
        fullPath.replace(
            collection_namePathParam,
            paramString +
                QUrl::toPercentEncoding(::qcai2::qdrant_client::toStringValue(collection_name)));
    }
    QString queryPrefix, querySuffix, queryDelimiter, queryStyle;
    if (wait.hasValue())
    {
        queryStyle = "form";
        if (queryStyle == "")
            queryStyle = "form";
        queryPrefix = getParamStylePrefix(queryStyle);
        querySuffix = getParamStyleSuffix(queryStyle);
        queryDelimiter = getParamStyleDelimiter(queryStyle, "wait", true);
        if (fullPath.indexOf("?") > 0)
            fullPath.append(queryPrefix);
        else
            fullPath.append("?");

        fullPath.append(QUrl::toPercentEncoding("wait"))
            .append(querySuffix)
            .append(QUrl::toPercentEncoding(
                ::qcai2::qdrant_client::toStringValue(wait.stringValue())));
    }
    if (ordering.hasValue())
    {
        queryStyle = "form";
        if (queryStyle == "")
            queryStyle = "form";
        queryPrefix = getParamStylePrefix(queryStyle);
        querySuffix = getParamStyleSuffix(queryStyle);
        queryDelimiter = getParamStyleDelimiter(queryStyle, "ordering", true);
        if (fullPath.indexOf("?") > 0)
            fullPath.append(queryPrefix);
        else
            fullPath.append("?");
        // For enum reference parameters, use direct string serialization instead of object
        // iteration
        QString enumValue = ordering.value().asJson();
        if (!enumValue.isEmpty())
        {
            fullPath.append(QUrl::toPercentEncoding("ordering"))
                .append("=")
                .append(QUrl::toPercentEncoding(enumValue));
        }
    }
    if (timeout.hasValue())
    {
        queryStyle = "form";
        if (queryStyle == "")
            queryStyle = "form";
        queryPrefix = getParamStylePrefix(queryStyle);
        querySuffix = getParamStyleSuffix(queryStyle);
        queryDelimiter = getParamStyleDelimiter(queryStyle, "timeout", true);
        if (fullPath.indexOf("?") > 0)
            fullPath.append(queryPrefix);
        else
            fullPath.append("?");

        fullPath.append(QUrl::toPercentEncoding("timeout"))
            .append(querySuffix)
            .append(QUrl::toPercentEncoding(
                ::qcai2::qdrant_client::toStringValue(timeout.stringValue())));
    }
    qdrant_HttpRequestWorker *worker = new qdrant_HttpRequestWorker(this, _manager);
    worker->setTimeOut(_timeOut);
    worker->setWorkingDirectory(_workingDirectory);
    worker->setResponseCompressionEnabled(_isResponseCompressionEnabled);
    worker->setRequestCompressionEnabled(_isRequestCompressionEnabled);
    qdrant_HttpRequestInput input(fullPath, "PUT");

    if (qdrant_point_insert_operations.hasValue())
    {

        QByteArray output = qdrant_point_insert_operations.value().asJson().toUtf8();
        input.request_body.append(output);
    }
    for (auto keyValueIt = _defaultHeaders.keyValueBegin();
         keyValueIt != _defaultHeaders.keyValueEnd(); keyValueIt++)
    {
        input.headers.insert(keyValueIt->first, keyValueIt->second);
    }

    connect(worker, &qdrant_HttpRequestWorker::downloadProgress, this,
            &qdrant_PointsApi::upsertPointsProgress);
    connect(worker, &qdrant_HttpRequestWorker::on_execution_finished, this,
            &qdrant_PointsApi::upsertPointsCallback);
    connect(this, &qdrant_PointsApi::abortRequestsSignal, worker, &QObject::deleteLater);
    connect(worker, &QObject::destroyed, this, [this] {
        if (findChildren<qdrant_HttpRequestWorker *>().count() == 0)
        {
            Q_EMIT allPendingRequestsCompleted();
        }
    });

    worker->execute(&input);
}

